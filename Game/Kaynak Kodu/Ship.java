public class Ship {
    private int x = 400;
    private int y = 200;

    public void setShip(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return this.y;
    }
    public int getX() {
        return this.x;
    }

}
